    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased

        String name = jTextField1.getText();
        if (name.equals("")) {
            try {
                loadData();
            } catch (SQLException ex) {
                Logger.getLogger(ViewBook.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        DefaultTableModel model = (DefaultTableModel) bookView.getModel();

        model.setNumRows(0);

        ResultSet rs = dbh.getData("select * from member where name like'" + name + "%'");
        if (rs != null) {

            try {
                while (rs.next()) {
                   
                     model.addRow(new Object[]{rs.getString("id"), rs.getString("name"), rs.getString("contact"), rs.getString("email")});

                }

            } catch (SQLException ex) {
                Logger.getLogger(ViewBook.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jTextField1KeyReleased

